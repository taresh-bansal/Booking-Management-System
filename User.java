package OOP_Project;

public class User {
	private String name;
	private String ID;
	private String clubName;
	
	public User(String name, String ID, String clubName){
		this.name=name;
		this.ID=ID;
		this.clubName=clubName;
	}
	
	public User(String name, String ID){
		this.name=name;
		this.ID=ID;
	}
	
	

}
